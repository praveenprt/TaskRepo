package Task36;

public class Person {
	private final int id;
	private final String Name;
	private final String PassWord;
	public Person(int id, String name, String passWord) {
		super();
		this.id = id;
		Name = name;
		PassWord = passWord;
	}
	@Override
	public String toString() {
		return "Person [id=" + id + ", Name=" + Name + ", PassWord=" + PassWord + "]";
	}
	

	
	
	


}
